
public class TypeCasting {

	public static void main(String[] args) {
		
		//implicit type Casting
		int num = 10;
		float data;
		data = num;
		System.out.println("Data :"+data);
		
		//explicit type Casting
		float value = 12.54f;
		int val;
		val = (int)value;
		System.out.println("Val :"+ val);
	}

}
