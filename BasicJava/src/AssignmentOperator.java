
public class AssignmentOperator {

	public static void main(String[] args) {
		
		int value1  = 10;
		int value2  = 20;
		
		value1 +=value2;
		System.out.println(value1);//30
		
		value1 *= value2;
		System.out.println(value1);
		
		value1 /=value2;
		System.out.println(value1);
		
		value1 -=value2;
		System.out.println(value1);
		

	}

}
